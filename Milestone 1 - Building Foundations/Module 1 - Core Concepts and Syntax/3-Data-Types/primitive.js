// Primitive Data Types:

// Number: Represents both integer and floating-point numbers.
// For example:
let num = 10; let floatNum = 3.14;

// String: Represents a sequence of characters enclosed within single ('') or double ("") quotes.
// For example: 
  let str = 'Hello, World!'; let anotherStr = "JavaScript";

// Boolean: Represents a logical entity, either `true` or `false`.
// For example: `
let isTrue = true; let isFalse = false;

// Undefined: Represents an uninitialized variable or a variable that has been declared but not assigned a value yet.
// For example: 
let undefinedVar;

// Null: Represents an intentional absence of any value.
// For example: 
let nullVar = null;