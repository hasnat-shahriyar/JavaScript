// Logical operators
let a = true;
let b = false;

let andOperator = a && b;  // Logical AND operator: Returns true if both a and b are true
console.log("AND Operator:", andOperator); // Output: AND Operator: false

let orOperator = a || b;  // Logical OR operator: Returns true if either a or b is true
console.log("OR Operator:", orOperator); // Output: OR Operator: true

let notOperator = !a;  // Logical NOT operator: Reverses the logical state of a
console.log("NOT Operator:", notOperator); // Output: NOT Operator: false