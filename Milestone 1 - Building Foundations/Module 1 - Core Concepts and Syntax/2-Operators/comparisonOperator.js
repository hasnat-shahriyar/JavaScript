// Comparison operators
let x = 5;
let y = 10;

let isEqual = x === y;  // Equality operator: Checks if x is equal to y
console.log("Is Equal:", isEqual); // Output: Is Equal: false

let isNotEqual = x !== y;  // Inequality operator: Checks if x is not equal to y
console.log("Is Not Equal:", isNotEqual); // Output: Is Not Equal: true

let greaterThan = x > y;  // Greater than operator: Checks if x is greater than y
console.log("Greater Than:", greaterThan); // Output: Greater Than: false

let lessThan = x < y;  // Less than operator: Checks if x is less than y
console.log("Less Than:", lessThan); // Output: Less Than: true

let greaterThanOrEqual = x >= y;  // Greater than or equal to operator: Checks if x is greater than or equal to y
console.log("Greater Than or Equal:", greaterThanOrEqual); // Output: Greater Than or Equal: false

let lessThanOrEqual = x <= y;  // Less than or equal to operator: Checks if x is less than or equal to y
console.log("Less Than or Equal:", lessThanOrEqual); // Output: Less Than or Equal: true

