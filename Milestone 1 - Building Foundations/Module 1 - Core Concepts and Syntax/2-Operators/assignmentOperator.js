// Assignment operator
let x = 10; // Assigns the value 10 to the variable x
console.log("x:", x); // Output: x: 10

let y;
y = 5; // Assigns the value 5 to the variable y
console.log("y:", y); // Output: y: 5

let z = x + y; // Assigns the result of the expression x + y to the variable z
console.log("z:", z); // Output: z: 15

// Compound assignment operator
let a = 20;
a += 5; // Equivalent to: a = a + 5
console.log("a:", a); // Output: a: 25

let b = 10;
b -= 3; // Equivalent to: b = b - 3
console.log("b:", b); // Output: b: 7

let c = 8;
c *= 2; // Equivalent to: c = c * 2
console.log("c:", c); // Output: c: 16

let d = 15;
d /= 3; // Equivalent to: d = d / 3
console.log("d:", d); // Output: d: 5

let e = 10;
e %= 3; // Equivalent to: e = e % 3
console.log("e:", e); // Output: e: 1
