// Arithmetic operators
let num1 = 10;
let num2 = 5;

let addition = num1 + num2;  // Addition operator: Adds num1 and num2
console.log("Addition:", addition); // Output: Addition: 15

let subtraction = num1 - num2;  // Subtraction operator: Subtracts num2 from num1
console.log("Subtraction:", subtraction); // Output: Subtraction: 5

let multiplication = num1 * num2;  // Multiplication operator: Multiplies num1 by num2
console.log("Multiplication:", multiplication); // Output: Multiplication: 50

let division = num1 / num2;  // Division operator: Divides num1 by num2
console.log("Division:", division); // Output: Division: 2

let modulus = num1 % num2;  // Modulus operator: Returns the remainder of dividing num1 by num2
console.log("Modulus:", modulus); // Output: Modulus: 0